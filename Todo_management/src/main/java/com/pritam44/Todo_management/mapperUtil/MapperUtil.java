package com.pritam44.Todo_management.mapperUtil;

import com.pritam44.Todo_management.dto.TodoDto;
import com.pritam44.Todo_management.entity.Todo;

public class MapperUtil {
	 public static TodoDto toDto(Todo todo) {
	        if (todo == null) return null;

	        TodoDto dto = new TodoDto();
	        dto.setId(todo.getId());
	        dto.setTitle(todo.getTitle());
	        dto.setDescription(todo.getDescription());
	        dto.setCompleted(todo.isCompleted());
	        dto.setCreatedDate(todo.getCreatedDate());
	        dto.setDueDate(todo.getDueDate());
	        dto.setPriority(todo.getPriority());
	        dto.setUserId(todo.getUserId());
	        return dto;
	    }

	    // Convert DTO -> Entity
	    public static Todo toEntity(TodoDto dto) {
	        if (dto == null) return null;

	        Todo todo = new Todo();
	        todo.setId(dto.getId());
	        todo.setTitle(dto.getTitle());
	        todo.setDescription(dto.getDescription());
	        todo.setCompleted(dto.isCompleted());
	        todo.setCreatedDate(dto.getCreatedDate());
	        todo.setDueDate(dto.getDueDate());
	        todo.setPriority(dto.getPriority());
	        todo.setUserId(dto.getUserId());
	        return todo;
	    }
	}


